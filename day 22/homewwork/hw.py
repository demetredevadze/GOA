#for loop-ის საშვალებით გამოიტანეთ ყველა რიცხვი 0-იდან 100-მდე
for i in range(1, 100):
   print(i)

#for loop-ის საშველბით გამოიტანეთ ყველა რიცხვი 20-დან 40 ის ჩათვლით
