// .toUpperCase() --> String-ის მეთოდია
// გამოიყენება ტექსტის ყველა ასოს დიდ ასოებად გადასაყვანად

let a = "hello"
console.log(a.toUpperCase())


// .toLowerCase() --> String-ის მეთოდია
// გამოიყენება ტექსტის ყველა ასოს პატარა ასოებად გადასაყვანად

let b = "HELLO"
console.log(b.toLowerCase())


// .startsWith() --> String-ის მეთოდია
// ამოწმებს იწყება თუ არა ტექსტი იმ სიტყვით სითყვით რომელიც მიუთითე

let c = "JavaScript"
console.log(c.startsWith("Java"))


// .trim() --> String-ის მეთოდია
// გამოიყენება ტექსტის დასაწყისსა და ბოლოში სფეისები მოსაშორებლად

let d = "   hello world   "
console.log(d.trim())